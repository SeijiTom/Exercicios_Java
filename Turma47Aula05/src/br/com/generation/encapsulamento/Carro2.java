package br.com.generation.encapsulamento;

public class Carro2 {
	
	//atributos --> variáveis
	private String marca;
	private String modelo;
	private String cor;
	private int numPassageiro;
	
//Métodos Getters e Setters --> métodos de acesso e modificação dos dados
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public int getNumPassageiro() {
		return numPassageiro;
	}
	public void setNumPassageiro(int numPassageiro) {
		this.numPassageiro = numPassageiro;
	}
	
}
